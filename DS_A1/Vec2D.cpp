#include "Vec2D.h"

/******************************************************************************/
/*!
\brief
Constructor with 2 double arguments - Create a Vec2D by assigning values to x and y

\param coordX - the value of x coordinate

\param coordY - the value of y coordinate
*/
/******************************************************************************/
Vec2D::Vec2D(double coordX, double coordY)
{
	x = coordX;
	y = coordY;
}